#include "libft.h"

int		ft_isnumber(char *str)
{
	int i;

	i = 0;
	if (str == NULL)
		return (0);
	while (str[i] > 32 && str[i] != ',')
	{
		if (!ft_isdigit(str[i]))
			return (0);
		i++;
	}
	return (1);
}
